fun main(args:Array<String>){

    var first=10
    var second =20

    // == operator
    println("equal operator ${first == second}")
    // > operator (grater)
    println("grater operator ${first > second}")
    // < operator (less)
    println("less operator ${first < second}")

    // >= operator (grater equal)
    println("grater equal operator ${first >= second}")
    // <= operator (less equal)
    println("less equal operator ${first <= second}")

    // not equal operator
    println("not equal operator ${first != second}")

}